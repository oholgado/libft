#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*node;

	while (*lst)
	{
		node = (*lst)-> next;
		(*del)((*lst)-> content);
		free(*lst);
		*lst = node;
	}
}
